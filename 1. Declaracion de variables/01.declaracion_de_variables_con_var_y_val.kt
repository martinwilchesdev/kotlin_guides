fun main() {
    // Variable mutable con un valor inicial de 5
    var v1 = 5
    // El valor de la variable puede modificarse durante la ejecucion del codigo
    v1 = 7

    // Variable inmutable con un valor inicial del "Hello"
    val v2 = "Hello"
    // El valor de la variable no puede ser modificado
    v2 = "Bye"
}