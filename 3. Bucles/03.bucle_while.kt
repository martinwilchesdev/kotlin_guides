fun main() {
    // El bucle while es una estructura utilizada para ejecutar un bloque de codigo mientras se cumpla una condicion
    
    /**
    * Si la condicion se evalua como verdadera, se ejecuta el bloque de codigo dentro del bucle
    * Si la condicion se evalua como falsa, el bucle se detiene y el control se pasa a la siguiente linea de codigo despues del bucle
    */
    var v1: Int = 1
    while(v1 <= 10) {
        println(v1)
        v1++
    }
}